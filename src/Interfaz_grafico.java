  public static String cadena()
  {
   Calendar cal = new GregorianCalendar();
   H=cal.get(Calendar.HOUR_OF_DAY);
   if(H<12)
   {
       cadena="am";
   }
   else
   {
       cadena="pm";
   }
   return cadena;
  }
  
