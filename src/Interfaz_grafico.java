    public static int dato(String dato)
    {
       String num=dato;
       String i=num.substring(0,2);
       String a=i.substring(1);
       String dat;
       if(":".equals(a))
       {
           dat=dato.substring(0,1);
           entero=Integer.parseInt(dat);
       }
       else
       {
           dat=dato.substring(0,2);
           entero=Integer.parseInt(dat);
       }
       
       return entero;
    }
   
