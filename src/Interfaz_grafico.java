   /**
    * 
    * @return se obtiene el numero de día de la semana..
    * del sistema.
    */
    public static String día()
        {
            
            GregorianCalendar cal = new GregorianCalendar();
            int dia=cal.get(Calendar.DAY_OF_WEEK);
            if(dia==2)
            {
               numdia="Día: "+"Lunes"; 
            }
            if(dia==3)
            {
              numdia="Día: "+"Martes";
            }
            if(dia==4)
            {
               numdia="Día: "+"Miercoles"; 
            }
            if(dia==5)
            {
              numdia="Día: "+"Jueves";
            }
            if(dia==6)
            {
               numdia="Día: "+"Viernes"; 
            }
            if(dia==7)
            {
              numdia="Día: "+"Sábado";
            }
            return numdia;
        }
     
